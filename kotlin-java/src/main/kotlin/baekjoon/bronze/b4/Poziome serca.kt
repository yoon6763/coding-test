package baekjoon.bronze.b4

fun main() {
    val n = readln().toInt()
    repeat(n) { print(" @@@   @@@  ") }
    println()
    repeat(n) { print("@   @ @   @ ") }
    println()
    repeat(n) { print("@    @    @ ") }
    println()
    repeat(n) { print("@         @ ") }
    println()
    repeat(n) { print(" @       @  ") }
    println()
    repeat(n) { print("  @     @   ") }
    println()
    repeat(n) { print("   @   @    ") }
    println()
    repeat(n) { print("    @ @     ") }
    println()
    repeat(n) { print("     @      ") }
}

