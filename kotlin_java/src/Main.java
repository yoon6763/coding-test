public class Main {
    public static void main(String[] args) {

    }

    static void print(int args1) {
        System.out.println(args1);
    }

    static void print(String args1, String args2) {
        System.out.println(args1 + " " + args2);
    }

    static void print(String args1, String args2, String args3) {
        System.out.println(args1 + " " + args2 + " " + args3);
    }

    static void print(String args1, String args2, String args3, String args4) {
        System.out.println(args1 + " " + args2 + " " + args3 + " " + args4);
    }

    static void print(String args1, String args2, String args3, String args4, String args5) {
        System.out.println(args1 + " " + args2 + " " + args3 + " " + args4 + " " + args5);
    }


}
